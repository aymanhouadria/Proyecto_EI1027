
CREATE TABLE Elderly(
DNI	 VARCHAR(10),
name   VARCHAR(50),
surname VARCHAR(20),
address VARCHAR(50),
birthDate VARCHAR(15),
phoneNumber VARCHAR(15),
bankAccountNumber VARCHAR(20), 
email VARCHAR(20),
userPwd VARCHAR(20),
dateCreation DATE,
alergies VARCHAR(20),
diseases VARCHAR(20),
userCAS VARCHAR(20),
CONSTRAINT cp_Eldely PRIMARY KEY (DNI),
CONSTRAINT ca_userCAS_S FOREIGN KEY (userCAS) REFERENCES SocialWorker
ON DELETE RESTRICT ON UPDATE CASCADE
);
